package roa.stats.swgoh;

public class CollectionSWGOH {
	private int gear_level;
	private int power;
	private int level;
	private String url;
	private int combat_type;
	private int rarity;
	private String player;

	public int getGear_level() {
		return gear_level;
	}

	public void setGear_level(int gear_level) {
		this.gear_level = gear_level;
	}

	public int getPower() {
		return power;
	}

	public void setPower(int power) {
		this.power = power;
	}

	public int getLevel() {
		return level;
	}

	public void setLevel(int level) {
		this.level = level;
	}

	public String getUrl() {
		return url;
	}

	public void setUrl(String url) {
		this.url = url;
	}

	public int getCombat_type() {
		return combat_type;
	}

	public void setCombat_type(int combat_type) {
		this.combat_type = combat_type;
	}

	public int getRarity() {
		return rarity;
	}

	public void setRarity(int rarity) {
		this.rarity = rarity;
	}

	public String getPlayer() {
		return player;
	}

	public void setPlayer(String player) {
		this.player = player;
	}
}